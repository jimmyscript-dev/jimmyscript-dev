const happy = true;

//*Regular If statement
if (happy === true) {
    console.log('Yay!')
} else {
    console.log('Nope.')
}

//*Using ternary operator for If statement.
happy === true ? console.log('Yay!') : console.log('Nope.');

const happy2 = false;

!happy2 ? console.log('Nope.') : console.log('Yay!');

